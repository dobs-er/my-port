// Update navigation buttons visibility
function updateNavButtons() {
  if (!currentProject || !currentProject.images) return;

  prevImageBtn.style.display = currentImageIndex > 0 ? "block" : "none";
  nextImageBtn.style.display =
    currentImageIndex < currentProject.images.length - 1 ? "block" : "none";
}

// Previous image button
prevImageBtn.addEventListener("click", () => {
  if (currentImageIndex > 0) {
    currentImageIndex--;
    updateMainImage();
    updateThumbnailSelection();
    updateNavButtons();
  }
});

// Next image button
nextImageBtn.addEventListener("click", () => {
  if (
    currentProject &&
    currentProject.images &&
    currentImageIndex < currentProject.images.length - 1
  ) {
    currentImageIndex++;
    updateMainImage();
    updateThumbnailSelection();
    updateNavButtons();
  }
});

